console.log("\nSubtração",)

const sub = (a,b) => {
    return a-b
}

console.log("15 + 10 =",sub(15,10))

//Multiplicação

console.log("\nMultiplicação",)

const multip = (a,b) => {
    return a*b
}

console.log("10 x 10 =",multip(10,10))

